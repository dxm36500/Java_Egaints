

public class Texascompany {

	
	String branch;
	String subbranch;
	
	public Texascompany(String branch, String subbranch) {
		
		this.branch = branch;
		this.subbranch = subbranch;
	}
	public String getBranch() {
		return branch;
	}
	public void setBranch(String branch) {
		this.branch = branch;
	}
	public String getSubbranch() {
		return subbranch;
	}
	public void setSubbranch(String subbranch) {
		this.subbranch = subbranch;
	}
	
	
}
